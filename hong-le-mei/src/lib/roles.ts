export const GENDERS = ['男', '女', '不限'] as const;
export type Gender = typeof GENDERS[number];

// 构建完整的用户 prompt（含行业上下文）
export function buildUserPrompt(params: {
  role: RoleKey;
  input: string;
  gender?: string;
  industry?: string;
  brandContext?: string;
}): string {
  const { role, input, gender, industry, brandContext } = params;
  const roleConfig = ROLES[role];

  let prompt = `【用户输入】${input}\n`;
  if (gender && gender !== '不限') prompt += `【目标性别】${gender}\n`;
  if (industry) {
    prompt += `【所属行业】${industry}\n`;
    const ctx = INDUSTRY_CONTEXT[industry];
    if (ctx) {
      prompt += `【行业高频词】${ctx.keywords.join('、')}\n`;
      prompt += `【行业痛点】${ctx.painPoints.join('、')}\n`;
      prompt += `【当前热门方向】${ctx.hotTopics.join('、')}\n`;
      prompt += `【平台内容习惯】${ctx.platformHabits}\n`;
      prompt += `【行业高发违禁词(务必避开)】${ctx.forbiddenWords.join('、')}\n`;
    }
    const scenario = roleConfig.industryScenarios[industry];
    if (scenario) prompt += `【角色场景】${scenario}\n`;
  }
  if (brandContext) prompt += `【品牌语料/产品信息】${brandContext}\n`;

  prompt += `\n【角色】${roleConfig.label}视角 — ${roleConfig.outputFocus}

请生成一条可直接发布的小红书笔记，严格按以下 JSON 格式输出（不要任何额外说明文字）：

{
  "titles": ["标题1（带emoji钩子）", "标题2", "标题3", "标题4", "标题5"],
  "body": "正文内容（分段、emoji适度、口语化、短段落，换行用\\n）",
  "tags": ["#话题1", "#话题2", "#话题3", "#话题4", "#话题5", "#话题6", "#话题7", "#话题8"],
  "platform": "小红书",
  "imageSuggestion": "配图建议：描述封面图应该呈现的画面和文字排版，给下一步生图用",
  "marketingLogic": [
    "钩子说明：这篇为什么能吸引点击，钩子类型是什么",
    "转化路径：从阅读到行动的完整路径",
    "目标人群：精准描述这篇想打动谁",
    "差异化：和同类内容相比的独特之处"
  ]
}`;

  return prompt;
}
